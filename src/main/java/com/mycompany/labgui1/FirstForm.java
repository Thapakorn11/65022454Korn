    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String Name = jTextField1.getText();
        String Gender = jComboBox1.getSelectedItem().toString();
        String Position = jComboBox2.getSelectedItem().toString();
        
        if(Name.isEmpty()||Gender.isEmpty()||Position.isEmpty()){
            JOptionPane.showMessageDialog(this,"Please Fill all data");
        }else{
            DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel(); 
            tblModel.addRow(new Object[]{Name,Gender,Position});
            jTextField1.setText("");
            jComboBox1.setSelectedIndex(0);
            jComboBox2.setSelectedIndex(0);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

