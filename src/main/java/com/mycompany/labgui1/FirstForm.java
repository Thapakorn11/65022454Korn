    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
    DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
    int row = jTable1.getSelectedRow();

    if (jTable1.getSelectedRowCount() == 1) {
        String name = String.valueOf(jTable1.getValueAt(row, 0));
        String gender = String.valueOf(jTable1.getValueAt(row, 1));
        String position = String.valueOf(jTable1.getValueAt(row, 2));
        tblModel.removeRow(row);
        JOptionPane.showMessageDialog(this, name + " "+ gender + " " + position + " was deleted: ");
    }
    }//GEN-LAST:event_jTable1MouseClicked

